################################################################################
# mod-lfg-all-dungeons - Configuration
################################################################################

[worldserver]

###############################################################################
# LFGAllDungeons.Enable
#   Active ou désactive entièrement le module.
#   Default : 1 (activé)
###############################################################################
LFGAllDungeons.Enable = 1

###############################################################################
# LFGAllDungeons.BypassLevel
#   Supprime les restrictions de niveau min/max dans le LFG.
#   Tous les joueurs peuvent rejoindre n'importe quel donjon
#   quelle que soit leur niveau.
#   Default : 1 (activé)
###############################################################################
LFGAllDungeons.BypassLevel = 1

###############################################################################
# LFGAllDungeons.BypassExpansion
#   Supprime les restrictions liées aux extensions (BC, WotLK…).
#   Un joueur sans l'extension peut quand même accéder aux donjons.
#   Default : 1 (activé)
###############################################################################
LFGAllDungeons.BypassExpansion = 1

###############################################################################
# LFGAllDungeons.Announce
#   Envoie un message au joueur quand il rejoint la file LFG
#   pour lui confirmer que les restrictions sont levées.
#   Default : 1 (activé)
###############################################################################
LFGAllDungeons.Announce = 1
